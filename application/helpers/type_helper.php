function merc_type($type)
	{
		switch($type)
			{
				case 0 :
					return 'Warrior';
				break;

				case 1 :
					return 'Holy Knight';
				break;

				case 2 :
					return 'Mage';
				break;

				case 3 :
				 return 'Archer';
				break;
			}
	};
